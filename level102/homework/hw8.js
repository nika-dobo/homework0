function num(num) {
  let even = 0;
  let odd = 0;
  let i = 1;
  while (i < num) {
    if (i % 2 == 0) {
      even++;
    } else {
      odd++;
    }
    i++;
  }
  console.log(even);
  console.log(odd);
}

num(34)
